#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		len;
	char	*dup;
	int		i;

	len = 0;
	while (str[len])
		len++;
	dup = malloc(sizeof(char) * (len + 1));
	i = 0;
	if (!dup)
		return (NULL);
	while (str[i])
	{
		dup[i] = str[i];
		i++;
	}
	dup[i] = 0;
	return (dup);
}
